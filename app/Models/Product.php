class Product extends Model
{
    use SoftDeletes;

    protected $guarded = [];

    public function categories()
    {
        return $this->belongsToMany('App\Models\Category', 'categories_products');
    }

    public function details()
    {
        return $this->hasOne('App\Models\ProductDetails', 'product_id');
    }
}
